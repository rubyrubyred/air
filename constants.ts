// Map normalized coordinates (-1 to 1) to types
export const mapCoordinatesToTypes = (x: number, y: number): { weather: WeatherType, mood: MoodType } => {
  // X Axis: Weather
  let weather = WeatherType.FOG;
  if (x < -0.6) weather = WeatherType.STORM;
  else if (x < -0.2) weather = WeatherType.RAIN;
  else if (x < 0.2) weather = WeatherType.FOG;
  else if (x < 0.6) weather = WeatherType.AURORA;
  else weather = WeatherType.SUN;

  // Y Axis: Mood
  let mood = MoodType.QUIET;
  if (y < -0.6) mood = MoodType.VOID;
  else if (y < -0.2) mood = MoodType.BROKEN;
  else if (y < 0.2) mood = MoodType.QUIET;
  else if (y < 0.6) mood = MoodType.JOY;
  else mood = MoodType.MANIC;

  return { weather, mood };
};